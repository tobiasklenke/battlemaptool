#include <QApplication>

#include "mainwindow.h"

/****************************************************************************************************************************************************
 * Constants
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * Global Variables
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * Global Functions
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * Local Functions
 ****************************************************************************************************************************************************/

/*!
 * \brief This function is the entry function of the application.
 */
int main(int argc, char *argv[])
{
    qDebug() << "..." << __func__;

    QApplication a(argc, argv);
    MainWindow w;

    /* show MainWindow and move it to the primary screen */
    w.showFullScreen();
    w.move(0,0);

    return a.exec();
}
